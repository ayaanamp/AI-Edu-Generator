from PyPDF2 import PdfReader
import re

def extract_clean_text(file):
    reader = PdfReader(file)
    text = ""

    for page in reader.pages:
        t = page.extract_text()
        if t:
            text += t + " "

    # CLEAN TEXT
    text = re.sub(r'\s+', ' ', text)  # remove extra spaces
    text = re.sub(r'[^A-Za-z0-9., ]+', '', text)  # remove junk symbols

    return text

