import random

def generate_mcqs(sentences, num_q=10):
    mcqs = []

    valid_sentences = [s for s in sentences if len(s.split()) > 10]

    for s in valid_sentences[:num_q]:
        words = list(set(s.split()))

        if len(words) < 6:
            continue

        answer = random.choice(words)
        question = s.replace(answer, "_____")

        options = random.sample(words, 4)
        if answer not in options:
            options[0] = answer

        random.shuffle(options)

        mcqs.append({
            "q": question,
            "options": options,
            "ans": answer
        })

    return mcqs

mcqs = generate_mcqs(sentences, 10)

print("Generated MCQs:", len(mcqs))

def run_quiz(mcqs):
    score = 0

    for i, q in enumerate(mcqs):
        print(f"\nQ{i+1}: {q['q']}")

        for idx, opt in enumerate(q['options']):
            print(f"{idx+1}. {opt}")

        user = input("Enter option number: ")

        try:
            user = int(user) - 1

            if 0 <= user < len(q['options']):
                if q['options'][user] == q['ans']:
                    print("✅ Correct")
                    score += 1
                else:
                    print(f"❌ Wrong → {q['ans']}")
            else:
                print("Invalid choice")
        except:
            print("Invalid input")

    print(f"\n🔥 Final Score: {score}/{len(mcqs)}")

run_quiz(mcqs)